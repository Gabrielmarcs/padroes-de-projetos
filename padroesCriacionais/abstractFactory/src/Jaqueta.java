public class Jaqueta {
    private String nome;
    private int qtdBotoes;
    public Jaqueta(String nome, int qtdBotoes) {
        this.nome = nome;
        this.qtdBotoes = qtdBotoes;
    }

    public String getNome() {
        return nome;
    }

    public int getQtdBotoes() {
        return qtdBotoes;
    }
}
